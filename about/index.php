        <p><b>WHAT IS TORREY PINES ABOUT?</p></b>
        <p>Torrey Pines is a stop-motion animated feature film by director Clyde Petersen. Based on a true story, the film is a queer punk coming-of-age tale, taking place in Southern California in the early 1990's.</p>
        <p>Raised by a schizophrenic single mother, Petersen's life story unfolds in a series of baffling and hallucinated events. With a mother fueled by hallucinations of political conspiracy and family dysfunction, Petersen is kidnapped at the age of 12 and taken on a cross-country adventure that will forever alter the family as they know it.</p>
