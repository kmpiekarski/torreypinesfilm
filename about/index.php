      <p><b>I DON'T UNDERSTAND. IS IT A MOVIE OR A THEATRICAL PIECE?</b></p>
      Torrey Pines is both a complete feature film, and a touring theatrical show. The film has a soundtrack produced by Chris Walla, while the live theatrical version has a score performed by the band Your Heart Breaks and special guests, including Kimya Dawson, Lori Goldston, The Beaconettes (an amazing chior from Seattle, Washington), Corey J. Brewer, Chris Looney and Art Petersen.
</p><br>
       <p><b>WHO IS CLYDE PETERSEN? WHAT'S THEIR DEAL?</p></b>
       Clyde Petersen is a Seattle based artist, working in film, animation, music, installation and fabulous spectacle. He is a member of the transgender and queer communities in Seattle. He travels the world with his punk band Your Heart Breaks and hosts a TV show in a small handmade boat in the Washington Park Arboretum. Boating with Clyde features artists from around the world, performing on the waterways of Seattle and can be seen <a href="http://www.boatingwithclyde.com" target='_blank'>online</a>. Past projects also include the 90-minute multimedia show Gender Failure with Rae Spoon and Ivan Coyote. <br>
<br>His work has been featured around the world at the London Lesbian and Gay Film Festival, SXSW, the Henry Art Gallery, the Walker Art Gallery, Portland State University, Western Washington University, Bumbershoot Arts Festival, Seattle Lesbian and Gay Film Festival and many more. His clients include The Thermals, Kimya Dawson, Laura Veirs, Earth, Minor Alps, Quasi, Deerhoof, Sean Nelson, The Portland Cello Project and Thao with the Get Down Stay Down to name a few.  He has been awarded grants for filmmaking from the New England Foundation for the Arts (NEFA), The Neddy at Cornish, the City of Seattle, the Artist Trust Innovator Award, the Awesome Foundation and 4Culture. 
